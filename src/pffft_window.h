const float * pffft_window(int audioBlockSize){
   if(       audioBlockSize ==  32){
      return hamming_window_32;
   }else if(audioBlockSize ==   64){
      return hamming_window_64;
   }else if(audioBlockSize ==  128){
      return hamming_window_128;
   }else if(audioBlockSize ==  256){
      return hamming_window_256;
   }else if(audioBlockSize ==  512){
      return hamming_window_512;
   }else if(audioBlockSize == 1024){
      return hamming_window_1024;
   }else if(audioBlockSize == 2048){
      return hamming_window_2048;
   }else if(audioBlockSize == 4096){
      return hamming_window_4096;
   }
   fprintf(stderr, "Window of size %d not supported.\n",audioBlockSize);
   return NULL;
}

